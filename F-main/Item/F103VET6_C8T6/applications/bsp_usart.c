// 计算CRC
unsigned char calcCRC(unsigned char *data, unsigned int len)
{
	unsigned char crc8 = 0;

    while (len --)
    {
        crc8 = crc8 ^ (*data++);
        crc8 = crc8Table[crc8];
    }

    return crc8;
}


